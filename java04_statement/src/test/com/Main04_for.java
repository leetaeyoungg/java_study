package test.com;

public class Main04_for {
    public static void main(String[] args) {
        System.out.println("hello");

        //for 반복문


        for (int i = 0; i < 10; i++) {
            System.out.println(i);

        }


        for (char i = '('; i < '\u0081'; i++) {
            System.out.printf("%c:%d\n",i,(int)i);

        }

    }//end main
}//end class
